number1 = int(input("Enter first number: "))
number2 = int(input("Enter second number: "))
operation = input("Enter operation: ")

if operation == "+":
    print(number1 + number2)
if operation =="-":
     print(number1- number2)
if  operation== "*":
     print(number1*number2)
if operation =="/":
     print(number1/number2) 
else:
     print ("You entered wrong operation")            
